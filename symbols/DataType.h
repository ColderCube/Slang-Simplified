#ifndef SYMBOLS_DATATYPE_H_
#define SYMBOLS_DATATYPE_H_


enum class Datatype {
    INT,          // Integer type
    FLOAT,        // Floating-point type
    CHAR,         // Character type
    STRING,       // String type
    BOOL,         // Boolean type
    ARRAY,        // Array type
    STRUCT,       // User-defined structure or class
    ENUM,         // Enumeration type
    POINTER,      // Pointer type
    VOID,         // Type representing absence of value
    FUNCTION,     // Function type
    USER_DEFINED  // User-defined types
};



#endif /* SYMBOLS_DATATYPE_H_ */
